#include <stdio.h>

int main()
{
    short a = 1;
    int b = 10;
    long c = 10;
    long long d = 10000;

    float e = 2e3;
    double g = 3e2;
    printf("short a = %d\n", a);
    printf("int b = %d\n", b);
    printf("long c = %ld\n", c);
    printf("long long d = %lld\n", d);
    printf("float e = %f\n", e);
    printf("double g = %lf\n", g);


    return 0;
}

